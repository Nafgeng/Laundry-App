            <p><strong>Pengertian Laundry</strong></p>

            <p>Laundry adalah bagian hotel yang memiliki tugas dan tanggung jawab terhadap semua cucian yang diterima kepadanya, baik dari house laundry ataupun dari guest laundry (tamu).

            Laundry dalam Bahasa Indonesia disebut dengan “binatu” atau “penatu” yang mempunyai makna usaha jasa yang bergerak dalam bidang pencucian pakaian atau lebih tepatnya mencuci dan menyetrika dengan hitungan kiloan berdasarkan berat material.

            Pengertian laundry menurut para ahli
            Menurut Rumekso SE,( 2001: hal 23), Laundry adalah seksi yang bertanggung jawab atas semua cucian yang dikirim kepadanya.
            Menurut Agustinus Darsono (1995: hal 89), laundry adalah bagian hotel yang bertanggung jawab terhadap pencucian, baik pencucian pakaian tamu, seragam karyawan maupun linen linen hotel.
            Menurut Bagyono (2006: hal 59), laundry adalah bagian dari housekeeping yang bertanggung jawab atas pencucian, baik pencucian pakaian tamu (laundry, dry cleaning dan pressing) seragam karyawan maupun linen linen hotel (house laundry) dan pencucian pakaian atau linen dari luar hotel (outside laundry).</p>

            <p><strong>Sejarah Laundry</strong></p>

            <p>Pada mulanya kegiatan laundy ini dilakukan menggunakan aliran air sungai untuk melarutkan kotoran pada pakaian.  karena pada saat itu belum ditembukan adanya sabun cuci atau bahkan tempat khusus yang menyediakan jasa pencucian.

            Jasa laundry pertama kali muncul pada jaman romawi kuno yang dikenal dengan sebutan Fullones (pencuci pakaian) yang bertugas membersihkan dan mencuci pakaian warga romawi. Pada saat itu pekerjaan laundry hanya dilakukan oleh seorang pria karena tugasnya cukup berat yakni menaruh cucian dalam tong kemudian di injak-injak dengan kaki dan dijemur sampai kering.

            Kemudian pada abad 18, jaman Eropa kuno proses cuci pakaian atau jasa laundry sudah lebih berkembang karena telah menggunkan sabun yang terbuat dari lemak hewani atau abu kali. Adapun sabun tersebut bisa membersihkan pakaian yang kotor dan menghilangkan noda dengan cukup baik.</p>

