        <div class="main_text">
            <p>Indonesia memiliki potensi besar dalam menjadi pasar utama bisnis laundry dengan melihat pertumbuhan signifikan yang terjadi dari tahun ke tahun. Selama periode 2021-2022 saja, usaha laundry di Indonesia diperkirakan tumbuh sebesar 50 persen. Hal ini terlihat dari banyaknya penetrasi dan perluasan bisnis yang dilakukan oleh sejumlah pelaku bisnis di industri ini.
                Melihat besarnya pertumbuhan bisnis ini, KasPro bersama dengan IZILOH, aplikasi Laundry Marketplace yang menghubungkan usaha laundry dengan para konsumen melalui berbagai fitur layanan yang ada, resmi mengumumkan kerjasamanya pada akhir Februari lalu.
                Kerjasama ini resmi berjalan dengan adanya penandatanganan yang dilakukan oleh Rully Hariwinata, Chief Marketing Officer KasPro dan Maria Eva Sriulina, CEO dan Founder IZILOH, pada acara Grand Launching IZILOH yang digelar 22 Februari 2023 lalu.</p>

            <p>KasPro yang bergerak sebagai layanan uang elektronik dan memiliki lisensi Bank Indonesia, hadir dalam aplikasi IZILOH untuk memberikan kenyamanan pembayaran bagi pengguna saat mereka melakukan transaksi di dalam aplikasi. Sebagai tahap awal, pengguna IZILOH sudah dapat melakukan registrasi akun KasPro mereka dan melakukan top up saldo sebagai alat pembayaran yang digunakan.</p>

            <p>“Melihat masifnya pasar laundry di Indonesia saat ini, membuat kami yakin untuk bekerja sama dengan IZILOH sebagai partner dalam melebarkan sayap bisnis kami di industri ini. Dengan komitmen yang tinggi dari IZILOH dalam mendorong ekosistemnya melakukan pemanfaatan sektor digital informasi, kami siap memberi dukungan teknologi demi mewujudkan peningkatan literasi digital yang lebih besar lagi terutama bagi mereka para pelaku di sektor laundry Indonesia,” ungkap Chief Marketing Officer KasPro Rully Hariwinata dalam keterangan tertulis di Jakarta, Jumat (3/3/2023).</p>

            <p>Dari kolaborasi ini, Rully mengatakan bahwa jangkauan pasar yang dimiliki oleh IZILOH memang sejalan dengan misi dari KasPro dalam meningkatkan angka literasi teknologi dan paham keuangan yang saat ini secara gencar dilakukan. Kerjasama dengan IZILOH ini juga nantinya akan berkembang dengan kehadiran layanan Cash-out (Bank Transfer & Modern Channel), P2P Transaction, QRIS, PPOB, dan tabungan emas (GoldPro) bagi pengguna IZILOH.</p>IZILOH sendiri yang menjadi jembatan antara penyedia dan pelanggan bisnis laundry, turut mendorong agar ekosistem ini mampu berevolusi dan beradaptasi dengan segala kondisi yang ada.

            <p>"Sinergi yang baik antara kami dan IZILOH diharapkan dapat mewujudkan revolusi baru di dunia laundry. Kami juga siap melangkah bersama IZILOH dalam mewujudkan masyarakat Indonesia yang lebih melek digital dan terus secara positif memiliki transformasi keuangan yang berkelanjutan dan mendorong peningkatan bisnis pengusaha laundry di Indonesia,” tutup Rully.</p>
