            <p>"Dari data ini dapat disimpulkan bahwa pemilik bisnis laundry pun semakin meningkat," kata Yusmin Joe, CEO Saku Laundry, saat Create Up IDX Channel di Jakarta, Kamis (2/2/2023).</p>

