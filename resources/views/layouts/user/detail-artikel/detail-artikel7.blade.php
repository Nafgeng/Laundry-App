            <p>''Sering saya temukan yang notanya hilang, uang dikasir tidak sesuai, dan sebagainya,'' jelas Yusmin.</p>

            <p>Dia juga menambahkan bahwa saat ini pihaknya sudah memiliki hampir 300 mitra yang tersebar di Jabodetabek, Kalimantan, dan juga Sumatra. Sistem kerja sama yang diterapkan adalah berlangganan dengan rentang harga dimulai dari Rp100 ribu per bulannya.</p>

            <p>"Jadi kami menyediakan banyak paket yang dapat dipilih oleh merchant sesuai kebutuhan," tandasnya.</p>
