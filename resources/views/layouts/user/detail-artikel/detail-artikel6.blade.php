            <p>Sektor usaha laundry kian menjanjikan seiring dengan kebutuhan kebutuhan rumah tangga dan industri. Terlebih lagi, selama pandemi, kebersihan seolah menjadi hal prioritas bagi siapapun.</p>

            <p>Sementara, di sisi laundry, pengusaha juga berusaha untuk meningkatkan kualitas dan hasil, bukan hanya dalam kapasitas laundry komersial dan industri, tetapi juga pada outlet laundry ritel seperti coin laundry dan kilo laundry. </p>

            <p>“Dalam upaya meningkatkan kualitas, tidak hanya dengan dukungan produk atau teknologi yang membantu proses kerja klining di berbagai sektor, namun, kompetensi dari tenaga kerja yang melaksanakan juga perlu ditingkatkan, terutama dengan pelatihan dan sertifikasi sehingga dipastikan penggunaan teknologi dan pengetahuan mengenai kebersihan dapat optimal,” kata H.M Shiddiq S.P, Ketua Umum Asosiasi Perusahaan Klining Servis Indonesia (Apklindo), dikutip Rabu (5/10/2022). </p>

            <p>Hal yang senada diungkapkan juga oleh Asosiasi Profesi Laundry Indonesia (APLI). Menurut Wasono Rahardjo, Ketua Umum APLI, tuntutan pasar akan jasa laundry yang bermutu dan memenuhi standar kesehatan turut mengubah peta dan pola kerja industri laundry nasional.</p>

            <p>“Terutama pada usaha laundry yang melayani sektor pariwisata dan fasilitas kesehatan, standar dan kriteria pelanggan semakin tinggi. Pelaku usaha harus terus meningkatkan mutu kalau tidak mau ketinggalan. Expo Laundry karena itu menjadi momen yang tepat dan sepatutnya dimanfaatkan oleh para pelaku usaha untuk mendapatkan update dan informasi produk dan teknologi,” katanya.</p>

            <p>PT Media Artha Sentosa telah menyelenggarakan “Expo Clean & Expo Laundry” sejak 2014. Kegiatan ini terus mendampingi dan mengantisipasi kebutuhan industri sehingga kami dapat menghadirkan   produk, teknologi dan solusi yang dibutuhkan.</p>

            <p>“Kali ini, salah satu peserta pameran PT Kawan Lama Sejahtera, akan meluncurkan produk baru dari Nilfisk di pameran ini. Kami berharap kegiatan ini dapat menjadi sinergi antara principal, importir, distributor dan pengguna barang dan jasa, pada platform yang komprehensif,” kata Teddy Halim, Direktur PT Media Artha Sentosa .</p>

            <p>Dengan dukungan penuh dari berbagai organisasi profesi dan industri, kegiatan ekspo juga menghadirkan berbagai seminar dari para organisasi pendukung, sehingga memberikan pengalaman pertukaran informasi dan pengalaman yang menyeluruh.</p>
